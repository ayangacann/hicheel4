# 15. Шатрын хөлөг дээр байрлах 2 тэмээний коардинат 
# x1, y1, x2, y2 хэлбэртэй өгөгдсөн бол 
# биесээ идэх бол true үгүй бол false гэж хэвлэ.

'''x бол 1-р тэмээ
y бол 2-р тэмээ

x = 1, 2
y = 1, 2
'''

x1, y1, x2, y2 = [int(i) for i in input()]
if x1 == x2 or y1 == y2:
    print("yes")
else: print("no")
